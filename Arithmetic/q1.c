#include<stdio.h>

int main() 
{
	int n,m;
	
	printf("Enter two numbers: \n");
	scanf("%d%d", &m, &n);
	
	printf("Addition: %d\n",m+n);
	printf("Subtraction: %d\n",m-n);
	printf("Multiplcation: %d\n",m*n);
	printf("Division: %d\n",m/n);

	return 0;
	
}
